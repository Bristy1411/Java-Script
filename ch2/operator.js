//*****Incremental******

var a = 5
console.log(a++) //post incremental
console.log(a)

var b = 12
console.log(++b) //pre incremental

//****Decremental*****

var m = 5
console.log(m--) //post decremental
console.log(m)

var n = 12
console.log(--n) //pre decremental

//*******comparison***** 

var x = '50'
var y = 50

console.log(x==y)
console.log(x===y) //compare data type and value
console.log(x!=y)
console.log(x!==y) //compare data type and value


