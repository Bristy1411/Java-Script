var date = new Date()

console.log(date) //2022-11-08T03:19:53.316Z
console.log(date.toDateString()) //Tue Nov 08 2022
console.log(date.toTimeString()) //09:26:25 GMT+0600 (Bangladesh Standard Time)
console.log(date.toLocaleString()) //11/8/2022, 9:27:48 AM
console.log(date.getFullYear()) //2022 (year)
console.log(date.getDate()) //8 (date)
console.log(date.getMonth()) //10 (month)